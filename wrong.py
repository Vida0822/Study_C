# 단원 종합 문제 17번 
a = [10, 20, 30, 40, 50, 60, 70, 80, 90]
print(a[3]) # 40
print(a[-5]) # 50 
print(a[:7:2]) # [10, 30, 50, 70]
print(a[3:]) # [40, 50, 60, 70, 80, 90]
print(a[:5]) # [10, 20, 30, 40, 50]

# 단원 종합 문제 18번 
sum = 0 
a = [1, 2, 3, 4, 5, 6, 7, 8]
b = a[::2]  # [1, 3, 5, 7]

for i in range(0,3): # 0 ~ 2 
    sum += b[i] # sum + b[0]=1 + b[1]=3 + b[2]=5 = 9 
print(sum) # 9

